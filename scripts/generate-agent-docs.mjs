#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { sourceTsdocRecordsForPackage } from "./public-api-model.mjs";

const root = process.cwd();
const check = process.argv.includes("--check");
const failures = [];

const posix = (file) => file.split(path.sep).join("/");
const rel = (file) => posix(path.relative(root, file));
const abs = (file) => path.join(root, file);
const exists = (file) => fs.existsSync(abs(file));
const read = (file) => fs.readFileSync(abs(file), "utf8");
const readJson = (file) => JSON.parse(read(file));

const generatedNotice = (source) =>
  `<!-- generated by scripts/generate-agent-docs.mjs; edit ${source} -->`;

const write = (file, text) => {
  const target = abs(file);
  const expected = `${text.replace(/\s+$/u, "")}\n`;
  if (check) {
    const actual = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : "";
    if (actual !== expected) failures.push(`${file} is stale`);
    return;
  }
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, expected);
};

const writeJson = (file, value) => write(file, JSON.stringify(value, null, 2));

const walk = (dir) => {
  const start = abs(dir);
  if (!fs.existsSync(start)) return [];
  const out = [];
  for (const entry of fs.readdirSync(start, { withFileTypes: true })) {
    const full = path.join(start, entry.name);
    if (entry.isDirectory()) {
      out.push(...walk(rel(full)));
      continue;
    }
    if (entry.isFile()) out.push(rel(full));
  }
  return out.sort((left, right) => left.localeCompare(right));
};

const relativeLink = (fromFile, targetFile) => {
  const fromDir = path.posix.dirname(fromFile);
  const target = path.posix.relative(fromDir, targetFile);
  return target.length === 0 ? path.posix.basename(targetFile) : target;
};

const mdLink = (fromFile, label, targetFile) => `[${label}](${relativeLink(fromFile, targetFile)})`;

const table = (headers, rows) => {
  const widths = headers.map((header, index) =>
    Math.max(header.length, ...rows.map((row) => row[index].length)),
  );
  const line = (row) =>
    `| ${row.map((cell, index) => `${cell}${" ".repeat(Math.max(0, widths[index] - cell.length))}`).join(" | ")} |`;
  return [
    line(headers),
    `| ${widths.map((width) => "-".repeat(width)).join(" | ")} |`,
    ...rows.map(line),
  ].join("\n");
};

const ensureUnique = (items, key, label) => {
  const seen = new Set();
  for (const item of items) {
    const value = key(item);
    if (seen.has(value)) failures.push(`duplicate ${label}: ${value}`);
    seen.add(value);
  }
};

const ensurePath = (file, owner) => {
  if (!exists(file)) failures.push(`${owner} references missing path ${file}`);
};

const surface = readJson("docs/surface.json");
const recipesSource = readJson("docs/agent/recipes.source.json");
const invariantsSource = readJson("docs/agent/invariants.source.json");
const errorsSource = readJson("docs/agent/error-metadata.source.json");
const externalVocabularySource = readJson("docs/agent/external-vocabulary.source.json");

ensureUnique(recipesSource.recipes, (recipe) => recipe.id, "recipe id");
ensureUnique(invariantsSource.invariants, (invariant) => invariant.id, "invariant id");
ensureUnique(errorsSource.errors, (error) => error.tag, "error tag metadata");
ensureUnique(externalVocabularySource.vocabulary, (entry) => entry.id, "external vocabulary id");

const invariantIds = new Set(invariantsSource.invariants.map((invariant) => invariant.id));
const errorMetadataByTag = new Map(errorsSource.errors.map((error) => [error.tag, error]));

for (const invariant of invariantsSource.invariants) {
  ensurePath(invariant.docs, invariant.id);
  for (const decision of invariant.decisions) ensurePath(decision, invariant.id);
  for (const test of invariant.tests) ensurePath(test, invariant.id);
}

const tagValues = (record, name) =>
  record.tags.filter((tag) => tag.name === name).map((tag) => tag.text).filter(Boolean);

const collectPrimitiveAnnotations = () => {
  const primitivesById = new Map();
  for (const pkg of surface.packages) {
    if (!exists(`${pkg.path}/package.json`)) continue;
    for (const record of sourceTsdocRecordsForPackage(root, pkg)) {
      const primitiveIds = tagValues(record, "agentosPrimitive");
      if (primitiveIds.length === 0) continue;
      if (primitiveIds.length > 1) {
        failures.push(`${pkg.name}:${record.key} has multiple @agentosPrimitive tags`);
        continue;
      }

      const docs = tagValues(record, "agentosDocs");
      const invariants = tagValues(record, "agentosInvariant");
      const decisions = tagValues(record, "agentosDecision");
      if (docs.length !== 1) {
        failures.push(`${pkg.name}:${record.key} must have exactly one @agentosDocs tag`);
      }
      if (invariants.length === 0) {
        failures.push(`${pkg.name}:${record.key} must have at least one @agentosInvariant tag`);
      }

      for (const invariant of invariants) {
        if (!invariantIds.has(invariant)) {
          failures.push(`${pkg.name}:${record.key} references unknown invariant ${invariant}`);
        }
      }
      for (const doc of docs) ensurePath(doc, `${pkg.name}:${record.key}`);
      for (const decision of decisions) ensurePath(decision, `${pkg.name}:${record.key}`);

      const primitive = {
        id: primitiveIds[0],
        package: pkg.name,
        packagePath: pkg.path,
        entrypoints: [record.entrypoint],
        symbol: record.name,
        exportKey: record.key,
        sourceFile: rel(record.file),
        summary: record.summary,
        aliases: tagValues(record, "agentosAlias"),
        invariants,
        decisions,
        docs: docs[0] ?? "",
      };

      const existing = primitivesById.get(primitive.id);
      if (existing === undefined) {
        primitivesById.set(primitive.id, primitive);
        continue;
      }
      if (
        existing.package !== primitive.package ||
        existing.symbol !== primitive.symbol ||
        existing.sourceFile !== primitive.sourceFile
      ) {
        failures.push(
          `primitive id ${primitive.id} is attached to multiple exported symbols: ${existing.package}:${existing.symbol} and ${primitive.package}:${primitive.symbol}`,
        );
        continue;
      }
      existing.entrypoints = [...new Set([...existing.entrypoints, ...primitive.entrypoints])].sort(
        (left, right) => left.localeCompare(right),
      );
    }
  }
  return [...primitivesById.values()].sort((left, right) => left.id.localeCompare(right.id));
};

const primitives = collectPrimitiveAnnotations();
ensureUnique(primitives, (primitive) => primitive.id, "primitive id");

const primitiveIds = new Set(primitives.map((primitive) => primitive.id));

for (const recipe of recipesSource.recipes) {
  ensurePath(recipe.tutorial, recipe.id);
  for (const primitive of recipe.primitives) {
    if (!primitiveIds.has(primitive)) {
      failures.push(`${recipe.id} references unknown primitive ${primitive}`);
    }
  }
  for (const evidence of recipe.evidence) ensurePath(evidence, recipe.id);
}

for (const entry of externalVocabularySource.vocabulary) {
  ensurePath(entry.docs, entry.id);
  for (const primitive of entry.mapsTo) {
    if (!primitiveIds.has(primitive)) {
      failures.push(`${entry.id} references unknown primitive ${primitive}`);
    }
  }
}

const agentOsTagPattern = /agent_os\.[a-z0-9]+(?:_[a-z0-9]+)*(?![a-z0-9_])/gu;
const discoverErrorTags = () => {
  const sources = new Map();
  const codeFiles = walk("packages").filter((file) => file.endsWith(".ts"));
  for (const file of codeFiles) {
    const text = read(file);
    for (const match of text.matchAll(agentOsTagPattern)) {
      const tag = match[0];
      const list = sources.get(tag) ?? [];
      if (!list.includes(file)) list.push(file);
      sources.set(tag, list);
    }
  }
  return [...sources.entries()]
    .map(([tag, sourceFiles]) => ({ tag, sourceFiles }))
    .sort((left, right) => left.tag.localeCompare(right.tag));
};

const discoveredErrors = discoverErrorTags();
for (const discovered of discoveredErrors) {
  const metadata = errorMetadataByTag.get(discovered.tag);
  if (metadata === undefined) {
    failures.push(`missing docs/agent/error-metadata.source.json entry for ${discovered.tag}`);
    continue;
  }
  for (const invariant of metadata.invariants) {
    if (!invariantIds.has(invariant)) {
      failures.push(`${discovered.tag} references unknown invariant ${invariant}`);
    }
  }
  ensurePath(metadata.docs, discovered.tag);
}

const errors = discoveredErrors
  .map((discovered) => {
    const metadata = errorMetadataByTag.get(discovered.tag);
    if (metadata === undefined) return null;
    return {
      tag: discovered.tag,
      invariants: metadata.invariants,
      docs: metadata.docs,
      fix: metadata.fix,
      sourceFiles: discovered.sourceFiles,
    };
  })
  .filter(Boolean);

const primitivesJson = {
  generatedBy: "scripts/generate-agent-docs.mjs",
  source: ["exported TSDoc @agentosPrimitive tags", "docs/agent/invariants.source.json"],
  primitives,
};

const recipesJson = {
  generatedBy: "scripts/generate-agent-docs.mjs",
  source: "docs/agent/recipes.source.json",
  recipes: recipesSource.recipes,
};

const errorsJson = {
  generatedBy: "scripts/generate-agent-docs.mjs",
  source: ["packages/**/*.ts agent_os.* tags", "docs/agent/error-metadata.source.json"],
  errors,
};

const unique = (values) => [...new Set(values)].sort((left, right) => left.localeCompare(right));

const invariantMatrix = invariantsSource.invariants.map((invariant) => {
  const invariantPrimitives = primitives
    .filter((primitive) => primitive.invariants.includes(invariant.id))
    .map((primitive) => primitive.id);
  const invariantErrors = errors
    .filter((error) => error.invariants.includes(invariant.id))
    .map((error) => error.tag);
  const docs = unique([
    invariant.docs,
    ...primitives
      .filter((primitive) => primitive.invariants.includes(invariant.id))
      .map((primitive) => primitive.docs),
    ...errors.filter((error) => error.invariants.includes(invariant.id)).map((error) => error.docs),
  ]);
  return {
    invariant: invariant.id,
    statement: invariant.statement,
    primitives: invariantPrimitives,
    errors: invariantErrors,
    docs,
    tests: invariant.tests,
    decisions: invariant.decisions,
  };
});

for (const row of invariantMatrix) {
  if (row.docs.length === 0) {
    failures.push(`${row.invariant} has no docs mapping`);
  }
  for (const test of row.tests) ensurePath(test, row.invariant);
}

const invariantMatrixJson = {
  generatedBy: "scripts/generate-agent-docs.mjs",
  source: [
    "docs/agent/invariants.source.json",
    "exported TSDoc @agentosInvariant tags",
    "docs/agent/error-metadata.source.json",
  ],
  invariants: invariantMatrix,
};

const startHere = [
  generatedNotice("docs/agent/*.source.json and exported TSDoc @agentosPrimitive tags"),
  "",
  "# Start Here",
  "",
  "Agent-facing docs are generated projections. Source facts live in `docs/agent/*.source.json`, `docs/surface.json`, package manifests, and exported TSDoc annotation tags.",
  "",
  "## Navigation",
  "",
  table(
    ["Surface", "Generated File"],
    [
      ["Recipes", mdLink("docs/start-here.md", "docs/agent/recipes.json", "docs/agent/recipes.json")],
      ["Primitives", mdLink("docs/start-here.md", "docs/agent/primitives.md", "docs/agent/primitives.md")],
      ["Errors", mdLink("docs/start-here.md", "docs/agent/errors.md", "docs/agent/errors.md")],
      [
        "Invariant Matrix",
        mdLink("docs/start-here.md", "docs/agent/invariant-matrix.md", "docs/agent/invariant-matrix.md"),
      ],
    ],
  ),
  "",
  "## Recipes",
  "",
  table(
    ["Recipe", "Tutorial", "Primary Primitives"],
    recipesSource.recipes.map((recipe) => [
      `\`${recipe.id}\``,
      mdLink("docs/start-here.md", recipe.title, recipe.tutorial),
      recipe.primitives.map((primitive) => `\`${primitive}\``).join(", "),
    ]),
  ),
  "",
].join("\n");

const primitivesMd = [
  generatedNotice("exported TSDoc @agentosPrimitive tags"),
  "",
  "# Agent Primitives",
  "",
  table(
    ["Primitive", "Export", "Invariant", "Docs"],
    primitives.map((primitive) => [
      `\`${primitive.id}\``,
      `\`${primitive.package}:${primitive.symbol}\``,
      primitive.invariants.map((invariant) => `\`${invariant}\``).join(", "),
      mdLink("docs/agent/primitives.md", path.posix.basename(primitive.docs), primitive.docs),
    ]),
  ),
  "",
].join("\n");

const errorsMd = [
  generatedNotice("packages/**/*.ts agent_os.* tags and docs/agent/error-metadata.source.json"),
  "",
  "# Agent Error Catalog",
  "",
  table(
    ["Error", "Invariant", "Docs", "Fix"],
    errors.map((error) => [
      `\`${error.tag}\``,
      error.invariants.map((invariant) => `\`${invariant}\``).join(", "),
      mdLink("docs/agent/errors.md", path.posix.basename(error.docs), error.docs),
      error.fix,
    ]),
  ),
  "",
].join("\n");

const invariantMatrixMd = [
  generatedNotice(
    "docs/agent/invariants.source.json, exported TSDoc @agentosInvariant tags, and docs/agent/error-metadata.source.json",
  ),
  "",
  "# Agent Invariant Matrix",
  "",
  table(
    ["Invariant", "Primitives", "Errors", "Docs", "Tests"],
    invariantMatrix.map((row) => [
      `\`${row.invariant}\``,
      row.primitives.map((primitive) => `\`${primitive}\``).join(", "),
      row.errors.map((error) => `\`${error}\``).join(", "),
      row.docs
        .map((doc) => mdLink("docs/agent/invariant-matrix.md", path.posix.basename(doc), doc))
        .join(", "),
      row.tests.map((test) => `\`${test}\``).join(", "),
    ]),
  ),
  "",
].join("\n");

if (failures.length === 0) {
  writeJson("docs/agent/recipes.json", recipesJson);
  writeJson("docs/agent/primitives.json", primitivesJson);
  write("docs/agent/primitives.md", primitivesMd);
  writeJson("docs/agent/errors.json", errorsJson);
  write("docs/agent/errors.md", errorsMd);
  writeJson("docs/agent/invariant-matrix.json", invariantMatrixJson);
  write("docs/agent/invariant-matrix.md", invariantMatrixMd);
  write("docs/start-here.md", startHere);
}

if (failures.length > 0) {
  console.error(failures.join("\n"));
  process.exit(1);
}

console.log(
  `generated agent docs: ${recipesSource.recipes.length} recipes, ${primitives.length} primitives, ${errors.length} errors`,
);
